// Program to print the sum of two numbers

# include <stdio.h> /* Preprocessor Directive */

/* Program Body */
int main()
{
    int num1,num2,sum; /* Variable declarations */

    num1 = 34;
    num2 = 54;
    
    sum = num1 + num2;  /* Sum computed */
    printf("Sum = %d\n",sum);

    return 0;   /* Terminates the program */
}
